#ifndef MATH_H_
#define MATH_H_

int math_add(int a, int b)
{
	return a+b;
}


int math_sub(int a, int b)
{
	return a-b;
}


long long math_mul(int a, int b)
{
	return a*b;
}


float math_div(int a, int b)
{
	return a/b;
}


int math_modulardiv(int a, int b)
{
	return a%b;
}


#endif /* MATH_H_ */
